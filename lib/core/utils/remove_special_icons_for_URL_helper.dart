  String removerAcentosParaUrl(String texto) {
    Map<String, String> mapaAcentos = {
      'á': 'a',
      'à': 'a',
      'ã': 'a',
      'â': 'a',
      'ä': 'a',
      'é': 'e',
      'è': 'e',
      'ê': 'e',
      'ë': 'e',
      'í': 'i',
      'ì': 'i',
      'î': 'i',
      'ï': 'i',
      'ó': 'o',
      'ò': 'o',
      'õ': 'o',
      'ô': 'o',
      'ö': 'o',
      'ú': 'u',
      'ù': 'u',
      'û': 'u',
      'ü': 'u',
      'ç': 'c',
    };

    texto = texto.toLowerCase();

    mapaAcentos.forEach((key, value) {
      texto = texto.replaceAll(key, value);
    });

    texto = texto.replaceAll(RegExp(r'\s+'), '');

    return texto;
  }